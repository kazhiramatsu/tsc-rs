//! Global names declared by the vendored default libraries — the
//! noLib-architecture escape for name-resolution failures.
//!
//! The conformance oracle checks every program WITH its target's
//! default lib set (harness resolve_program_libs), while this engine
//! deliberately binds NO lib files (M4 5.0 decision). A name that the
//! libs declare therefore RESOLVES in the oracle's world: emitting
//! "cannot find" for it here would be a noLib artifact, not a tsc
//! observable, so the resolver's failure path skips these names (an
//! FN against @noLib/@lib-restricted fixtures, where tsc genuinely
//! reports). The table is the union of top-level declaration names
//! (var/function/class/interface/type/enum/namespace) across
//! vendor/typescript-6.0.3/lib/lib.*.d.ts, extracted mechanically and
//! sorted for binary search. It retires whole when lib loading lands.

pub(crate) fn is_default_lib_global_name(name: &str) -> bool {
    DEFAULT_LIB_GLOBAL_NAMES.binary_search(&name).is_ok()
}

